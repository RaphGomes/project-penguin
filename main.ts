namespace SpriteKind {
    export const Button = SpriteKind.create()
    export const Misc = SpriteKind.create()
}
controller.A.onEvent(ControllerButtonEvent.Pressed, function () {
    if (Menu == 0) {
        jumping_penguin.destroy()
        Menu = 1
        Menu_Control()
    }
})
function Menu_Control () {
    if (Menu == 0) {
        scene.setBackgroundImage(assets.image`Start Screen`)
        jumping_penguin = sprites.create(assets.image`myImage0`, SpriteKind.Misc)
        jumping_penguin.setPosition(145, 110)
        animation.runImageAnimation(
        jumping_penguin,
        assets.animation`A button`,
        500,
        true
        )
    }
    if (Menu == 1) {
        scene.setBackgroundImage(assets.image`Menu Screen`)
        Button_1 = sprites.create(assets.image`Game Button`, SpriteKind.Button)
        Button_1.setPosition(37, 98)
        Button_2 = sprites.create(assets.image`Help Button`, SpriteKind.Button)
        Button_2.setPosition(124, 98)
        Cursor = sprites.create(assets.image`Cursor`, SpriteKind.Player)
        Cursor.setPosition(80, 98)
        controller.moveSprite(Cursor, 80, 80)
        Cursor.setBounceOnWall(true)
    }
    if (Menu == 2) {
        Button_1.destroy()
        Button_2.destroy()
        Cursor.destroy()
        scene.setBackgroundImage(assets.image`Small Map`)
    }
    if (Menu == 2) {
        Button_1.destroy()
        Button_2.destroy()
        Cursor.destroy()
        scene.setBackgroundImage(assets.image`Small Map`)
        story.startCutscene(function () {
            jumping_penguin = sprites.create(assets.image`Mr Penguin 3`, SpriteKind.Player)
            animation.runImageAnimation(
            jumping_penguin,
            assets.animation`jumping penguin`,
            250,
            true
            )
            story.spriteSayText(jumping_penguin, "Press", 1, 15, story.TextSpeed.Slow)
        })
    }
}
sprites.onOverlap(SpriteKind.Player, SpriteKind.Button, function (sprite, otherSprite) {
    if (otherSprite == Button_2 && controller.A.isPressed()) {
        Menu = 2
        Menu_Control()
    }
    if (otherSprite == Button_1 && controller.A.isPressed()) {
        Menu = 3
        Level_Control()
    }
})
